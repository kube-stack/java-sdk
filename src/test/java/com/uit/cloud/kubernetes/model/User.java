package com.uit.cloud.kubernetes.model;

public class User {

	protected String name;
	
	protected int age;

	public String getName() {
		return name;
	}

	protected void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	protected void setAge(int age) {
		this.age = age;
	}
	
}
